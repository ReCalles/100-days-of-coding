# 1. New Line Escape Sequence

# Learn to use `\n` to add a new line to strings.

print("Hello")
print("Word")

# is the same as:

print("Hello\nWorld")


# PAUSE 2. Add a space between the strings
# So there is a space between the string Hello and Angela when the print statement runs.
#
# The output should look like this:
#
# Hello Angela

print("Hello Angela")

# 2. String Concatenation
# Use the plus symbol `+` to concatenate different strings together

print("My name is" + " " + "Angela")

# will output: "My name is Angela"
